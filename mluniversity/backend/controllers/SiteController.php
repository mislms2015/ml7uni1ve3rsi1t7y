    /**
     * Login action.
     *
     * @return string
     */
    public function actionLogin()
    {
        $this->layout = 'loginLayout';
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            /* check profile */
            $check_profile = UserProfile::find()->where(['user_id' => Yii::$app->user->identity->id])->count();
            if ($check_profile == 0){
                Yii::$app->db->createCommand()
                     ->Insert('user_profile', [
                        'user_id' => Yii::$app->user->identity->id,
                        'firstname' => '-',
                        'middlename' => '-',
                        'lastname' => '-',
                        'avatar_path' => '-',
                        'avatar_base_url' => '-',
                        ])
                     ->execute();
            }
            /* check profile */
            return $this->goBack();
            //return $this->redirect(Url::to(['/site/index']));
        } else {
            return $this->render('login', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Logout action.
     *
     * @return string
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }
}

